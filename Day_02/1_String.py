# [1]
myStringOne = 'Amir'
myStringSecond = "Amir"
myStringThird = "Amir 'Ibrahim'"

print(myStringOne)  # Amir
print(myStringSecond)  # Amir
print(myStringThird)  # Amir 'Ibrahim'

# [2]
myMultiline = "amir\nibrahim\nElka-sed\n"
myMultilineSec = """Amir
Ibrahim
Elka-sed"""
print(myMultiline)
print(myMultilineSec)
